package bean;


public class DadosSignos {

    private String codigoSigno;
    private String nomeSigno;

    public String getCodigoSigno() {
        return codigoSigno;
    }

    public void setCodigoSigno(String codigoSigno) {
        this.codigoSigno = codigoSigno;
    }

    public String getNomeSigno() {
        return nomeSigno;
    }

    public void setNomeSigno(String nomeSigno) {
        this.nomeSigno = nomeSigno;
    }
    
}
